package ru.pastor.templates.named.repository.entity;

import lombok.Builder;

import java.time.LocalDateTime;

/**
 * Сущность для представления элемента каталога счетчиков в базе данных.
 * Содержит информацию о типе счетчика, его названии и описании.
 * Реализована как неизменяемая запись (record) с возможностью создания через Builder.
 */
@Builder(toBuilder = true)
public record CatalogueEntity(
  /**
   * Уникальный идентификатор элемента каталога в базе данных.
   * Генерируется автоматически при создании записи.
   */
  Integer id,

  /**
   * Название типа счетчика.
   * Используется для идентификации типа счетчика в запросах.
   */
  String name,

  /**
   * Описание типа счетчика.
   * Содержит дополнительную информацию о назначении и использовании счетчика.
   */
  String description,

  /**
   * Дата и время создания элемента каталога.
   */
  LocalDateTime created,

  /**
   * Дата и время последнего обновления элемента каталога.
   */
  LocalDateTime updated
) {
}
