package ru.pastor.templates.named.repository.entity;

import lombok.Builder;

import java.time.LocalDateTime;

/**
 * Сущность для представления счетчика в базе данных.
 * Содержит информацию о счетчике, его значении и связанных метаданных.
 * Реализована как неизменяемая запись (record) с возможностью создания через Builder.
 */
@Builder(toBuilder = true)
public record CounterEntity(
  /**
   * Уникальный идентификатор записи счетчика в базе данных.
   * Генерируется автоматически при создании записи.
   */
  Integer id,

  /**
   * Связанная сущность каталога, содержащая информацию о типе счетчика.
   * Представляет отношение многие-к-одному между счетчиками и элементами каталога.
   */
  CatalogueEntity catalogue,

  /**
   * Идентификатор пользователя, которому принадлежит счетчик.
   * Используется для фильтрации счетчиков по пользователю.
   */
  long userId,

  /**
   * Текущее значение счетчика.
   */
  long value,

  /**
   * Дата и время создания записи счетчика.
   */
  LocalDateTime created,

  /**
   * Дата и время последнего обновления записи счетчика.
   */
  LocalDateTime updated
) {
}
